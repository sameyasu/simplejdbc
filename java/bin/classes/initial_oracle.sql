
CREATE TABLE GOODS_MST
(
    GOODS_ID                       NUMBER(10) NOT NULL,		
    GOODS_NAME                     VARCHAR2(100) NOT NULL,	
    PRICE                          NUMBER(10,2),			
    REGIST_DATE                    DATE,					
    UPDATE_DATE                    DATE,					
    VERSION                        VARCHAR2(17),			
    CONSTRAINT GOODS_MST_PKEY PRIMARY KEY (GOODS_ID)
)
/


CREATE TABLE GOODS_UPDATE_TRANS
(
    TRANS_ID                       VARCHAR2(15) NOT NULL,	
    GOODS_ID                       NUMBER(10) NOT NULL,		
    UPDATE_KIND                    VARCHAR2(1) NOT NULL,	
    UPDATE_DATE                    DATE NOT NULL,			
    VERSION                        VARCHAR2(17),			
    CONSTRAINT GOODS_UPDATE_TRANS_PKEY PRIMARY KEY (TRANS_ID)
)
/


CREATE TABLE GOODS_MST_NOVERSION
(
    GOODS_ID                       NUMBER(10) NOT NULL,		
    GOODS_NAME                     VARCHAR2(100) NOT NULL,	
    PRICE                          NUMBER(10,2),			
    REGIST_DATE                    DATE,					
    UPDATE_DATE                    DATE,					
    CONSTRAINT GOODS_MST_NOVERSION_PKEY PRIMARY KEY (GOODS_ID)
)
/


CREATE TABLE DUMMY_TABLE
(
    DATE_COLUMN                      DATE,
    TIMESTAMP_COLUMN                 TIMESTAMP,
    BYTE_COLUMN                      NUMBER(3,0),
    CHAR_COLUMN                      NUMBER(5,0),
    SHORT_COLUMN                     NUMBER(5,0),
    INT_COLUMN                       NUMBER(10,0),
    LONG_COLUMN                      NUMBER(19,0),
    BIGINTEGER_COLUMN                NUMBER(30,0),
    FLOAT_COLUMN                     NUMBER(10,5),
    DOUBLE_COLUMN                    NUMBER(20,10),
    CLOB_COLUMN                      CLOB,
    CONSTRAINT DUMMY_TABLE_PKEY PRIMARY KEY (DATE_COLUMN)
)
/


CREATE SEQUENCE SEQ_GOODS_ID
INCREMENT BY 1
START WITH 100
MAXVALUE 9999999999
NOMINVALUE
CYCLE
NOCACHE
NOORDER
/


CREATE SEQUENCE SEQ_TRANS_ID
INCREMENT BY 1
START WITH 1
MAXVALUE 999999999999
NOMINVALUE
CYCLE
NOCACHE
NOORDER
/


